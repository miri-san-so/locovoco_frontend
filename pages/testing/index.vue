<script>
export default {
    data() {
        return {
            open: "",
            random_avatar: 1
        };
    },
    methods: {
        randomIntFromInterval(min, max) {
            return Math.floor(Math.random() * (max - min + 1) + min);
        },
        increment() {
            if (this.random_avatar >= 5) this.random_avatar = 0;
            if (this.random_avatar <= 0) this.random_avatar = 0;
            if (this.random_avatar >= 0 || this.random_avatar < 5)
                this.random_avatar++;
            localStorage.setItem("avatar_id", this.random_avatar);
        }
    },
    mounted() {
        window.scrollTo(0, 0);
        this.$store.commit("showTab");
        this.$store.dispatch("setCurrentPage");
        this.random_avatar =
            localStorage.avatar_id !== undefined
                ? localStorage.avatar_id
                : this.randomIntFromInterval(1, 5);
    },
    beforeDestroy() {
        this.$store.commit("hideTab");
    }
};
